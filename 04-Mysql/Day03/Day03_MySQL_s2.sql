-- ============================================================
-- [개념 10] GROUP BY / HAVING
-- ============================================================
--
-- GROUP BY
--   - 같은 값을 가진 행을 그룹으로 묶는다.
--   - 집계 함수(SUM, AVG, COUNT 등)와 함께 사용한다.
--   - GROUP BY 절에 없는 컬럼은 SELECT 에 단독 사용 불가
--     (집계 함수로 감싸야 한다.)
--
-- HAVING
--   - GROUP BY 이후 만들어진 그룹 결과에 조건을 건다.
--   - WHERE 는 그룹 전 개별 행 조건
--   - HAVING 은 그룹 후 집계 결과 조건
--   - HAVING 은 반드시 GROUP BY 다음에 위치해야 한다.
--


