-- ============================================================
-- [개념] Window Function - 행 순위와 누적 계산
-- ============================================================
--
-- Window Function은 행을 그룹으로 접지 않고, 각 행을 유지한 상태에서
-- 순위, 누적합, 그룹별 합계 등을 계산할 때 사용한다.
--
-- 기본 형식:
--   함수() OVER (
--     PARTITION BY 그룹컬럼
--     ORDER BY 정렬컬럼
--   )
--
-- 자주 쓰는 함수:
--   ROW_NUMBER() : 순번
--   RANK()       : 공동 순위가 있으면 다음 순위를 건너뜀
--   DENSE_RANK() : 공동 순위가 있어도 다음 순위를 건너뛰지 않음
--   SUM() OVER() : 누적합 / 그룹별 합계
--
-- GROUP BY와 차이:
--   GROUP BY         : 여러 행을 하나의 결과 행으로 묶는다.
