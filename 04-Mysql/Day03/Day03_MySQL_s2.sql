-- ============================================================
-- [개념] 서브쿼리 분류
-- ============================================================
--
-- 서브쿼리는 두 가지 기준으로 분류한다.
--
-- ── 기준 1: 결과의 형태 ──
-- 1. 단일행 서브쿼리   -> 결과가 1행 1열 (값 1개)
--                        =, >, <, >=, <= 사용
-- 2. 다중행 서브쿼리   -> 결과가 여러 행
--                        IN, ANY, ALL 사용
-- 3. 다중열 서브쿼리   -> 결과가 여러 컬럼
--                        (컬럼1, 컬럼2) IN (...)
--
-- ── 기준 2: 메인쿼리 참조 여부 ──
-- A. 비연관 서브쿼리   -> 독립적으로 1번만 실행
-- B. 연관(상관) 서브쿼리 -> 메인쿼리 값을 사용, 행마다 반복 실행
--                          EXISTS, 스칼라 서브쿼리에서 자주 사용
--
-- ── 사용 위치에 따른 특수 형태 ──
-- - 스칼라 서브쿼리 : 결과가 1개 값인 서브쿼리
--                    SELECT, WHERE, ORDER BY 절에서 사용
-- - 인라인 뷰      : FROM 절에서 사용하는 서브쿼리
--                    임시 테이블처럼 동작
--


