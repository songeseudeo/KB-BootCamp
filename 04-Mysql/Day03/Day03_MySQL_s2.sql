-- ============================================================
-- [개념 17] DELETE / TRUNCATE / DROP 차이
-- ============================================================
--
-- DELETE
--   - DML
--   - 행 단위 삭제 (WHERE 사용 가능)
--   - LIMIT 사용 가능
--   - 테이블 구조 유지
--   - 트랜잭션 로그 기록 -> 속도 느림
--
-- TRUNCATE
--   - DDL 성격
--   - 모든 행을 빠르게 삭제 (WHERE 불가)
--   - 테이블 구조 유지
--   - AUTO_INCREMENT 값 초기화
--   - 트랜잭션 없음 -> 속도 빠름, ROLLBACK 불가
--   - 외래 키로 다른 테이블과 연결된 경우 실패할 수 있음
--
-- DROP
--   - DDL
--   - 테이블 자체(구조+데이터) 삭제
--
-- 비교표:
--   명령어    | 구조 유지 | 조건 WHERE | ROLLBACK | AUTO_INCREMENT
--   DELETE    | O         | O          | 가능     | 유지
--   TRUNCATE  | O         | X          | 불가     | 초기화
--   DROP      | X         | X          | 불가     | 테이블 삭제
--
-- 실무 주의 :
--   DROP / TRUNCATE 는 되돌릴 수 없다. 반드시 대상 확인 후 실행.
--


