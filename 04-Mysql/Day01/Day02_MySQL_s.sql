-- ============================================================
-- [개념 1] 데이터베이스 모델링이란?
-- ============================================================
--
-- 데이터베이스 모델링(Database Modeling)은
-- 현실 세계의 업무나 사물을 DBMS의 데이터베이스 개체로 옮기기 위한 과정이다.
--
-- [현실 세계 -> 데이터베이스]
-- 쇼핑몰 현실 데이터   ->   DB 구조
-- 고객(사람)           ->   memberTBL (회원 테이블)
-- 진열된 물건          ->   productTBL (제품 테이블)
-- 고객이 물건을 산다   ->   buyTBL (구매 테이블)
--
-- 비유: 도서관을 예로 들면
-- 도서관 전체 = 데이터베이스
-- 책장(서가)  = 테이블
-- 책 한 권    = 행(Row), 데이터 1건
-- 책의 속성   = 열(Column) : 제목, 저자, 출판연도 ...


-- ============================================================
-- [개념 2] 모델링 3단계
-- ============================================================
--
-- 1. 개념적 모델링
--    - 업무 분석 단계에서 진행
--    - 업무에서 필요한 대상(Entity)을 찾는다.
--    - 예: 쇼핑몰에는 "회원", "상품", "구매" 가 필요하다.
--
-- 2. 논리적 모델링
--    - 업무 분석 후반 ~ 시스템 설계 전반에 진행
--    - 테이블, 컬럼, 테이블 간의 관계(Relation)를 설계한다.
--    - 예: 회원 테이블에는 아이디, 이름, 주소가 필요하다.
--         회원 1명은 여러 번 구매할 수 있다. (1:N 관계)
--
-- 3. 물리적 모델링
--    - 시스템 설계 후반에 진행
--    - 실제 MySQL 문법으로 테이블을 생성한다.
--    - 예: CREATE TABLE memberTBL (...)


-- ============================================================
-- [개념 3] 왜 테이블을 분리하는가?
-- ============================================================
--
-- 하나의 큰 표에 고객 정보 + 구매 정보를 모두 넣으면 아래 문제가 생긴다.
--
-- [문제 1] 중복 데이터
--   고객이 3번 구매하면 이름, 주소, 연락처가 3번 반복 저장된다.
--
-- [문제 2] L자형 낭비
--   구매하지 않은 고객 행에서 구매 관련 컬럼이 모두 빈칸(NULL)이 된다.
--
-- [문제 3] 수정 어려움
--   고객 주소가 바뀌면 그 고객의 모든 행을 찾아 수정해야 한다.
--
-- [해결책] 데이터를 성격에 따라 테이블로 분리한다.
--   고객 정보  -> memberTBL  (부모 테이블)
--   구매 정보  -> buyTBL     (자식 테이블)
--   상품 정보  -> productTBL (독립 테이블)


-- ============================================================
-- [개념 4] 핵심 용어 정리
-- ============================================================
--
-- Entity(엔터티)   : 데이터를 저장할 대상, 즉 테이블
-- Attribute(속성) : 엔터티가 가지는 정보 항목, 즉 컬럼(Column)
-- Row(행)          : 실제 데이터 1건 (가로 한 줄)
-- Column(열)       : 데이터 항목 (세로 한 줄)
--
-- 예:
--   memberTBL              = Entity (엔터티)
--   memberID, memberName   = Attribute (속성)
--   ('Dang', '당탕이', '경기 부천시 중동') = Row 1건


-- ============================================================
-- [개념 5] 제약조건 (Constraint)
-- ============================================================
--
-- PRIMARY KEY (기본 키)
--   - 각 행을 유일하게 구분하는 대표 값
--   - 중복 불가 + NULL 불가
--   - 예: memberID가 'Dang' 인 회원은 오직 1명만 존재해야 함
--
-- NOT NULL
--   - 해당 컬럼에 반드시 값이 있어야 함
--   - 예: 회원 이름(memberName)은 반드시 입력해야 함
--
-- FOREIGN KEY (외래 키)
--   - 다른 테이블의 PRIMARY KEY를 참조하는 키
--   - 부모 테이블의 PK = 자식 테이블의 FK
--   - 예: buyTBL의 memberID -> memberTBL의 memberID 참조
--   - 제약: 부모 테이블에 없는 값은 자식 테이블에 넣을 수 없음


-- ============================================================
-- [개념 6] 데이터 타입
-- ============================================================
--
-- CHAR(n)
--   - 고정 길이 문자열, 항상 n글자만큼 공간 사용
--   - 예: CHAR(8) -> 'Dang' 저장 시 내부적으로 8자리로 저장
--   - 길이가 거의 변하지 않는 코드, ID에 적합
--
-- VARCHAR(n)
--   - 가변 길이 문자열, 실제 입력한 길이만큼만 공간 사용
--   - 예: VARCHAR(256) -> '안녕하세요' 저장 시 실제 길이만 사용
--   - 길이가 자주 변하는 이름, 주소, 제목 등에 적합
--   - 참고: 본 실습 기본은 교재 예제 기준에 맞춰 CHAR 사용
--
-- INT
--   - 정수 (약 -21억 ~ +21억)
--   - 예: 가격, 수량, 나이
--
-- DATE
--   - 날짜 형식 (YYYY-MM-DD)
--   - 예: 2021-01-01
--
-- DATETIME
--   - 날짜 + 시각 형식 (YYYY-MM-DD HH:MM:SS)
--   - 예: 2024-07-02 13:37:07
--
-- TEXT
--   - 긴 문자열 (최대 65,535 바이트)
--   - 예: 게시글 본문, 상품 설명
--
-- DECIMAL(p, s)
--   - 소수점이 있는 숫자, p: 전체 자릿수, s: 소수점 이하 자릿수
--   - 예: DECIMAL(5, 2) -> 최대 999.99


-- ============================================================
-- [개념 7] 테이블 간 관계 (Relation) - 1:N 관계
-- ============================================================
--
-- 1:N 관계 (가장 흔한 관계)
--   - 부모 테이블 1건 <-> 자식 테이블 여러 건
--   - 예: 회원 1명이 구매 여러 번 -> memberTBL(1) : buyTBL(N)
--
-- 규칙
--   - 새 데이터를 넣을 때 : 부모 테이블에 먼저 넣어야 함
--   - 데이터를 삭제할 때  : 자식 테이블에서 먼저 지워야 함
--
-- [쇼핑몰 예제 관계 구조]
--
--    memberTBL (부모)            buyTBL (자식)
--    +---------------+    1:N   +---------------+
--    | memberID (PK) |<---FK----| memberID (FK) |
--    | memberName    |          | prodName      |
--    | birthYear     |          | price         |
--    | addr          |          | amount        |
--    | mobile        |          +---------------+
--    +---------------+
--
-- 부모 테이블의 Primary Key = 자식 테이블의 Foreign Key




-- ============================================================
-- [실습 1] shopdb 데이터베이스 생성
-- ============================================================
--
-- 주의 : DROP DATABASE는 DB 안의 모든 테이블과 데이터를 삭제한다.
--
-- shopdb 데이터베이스 삭제
DROP DATABASE shopdb;

-- shopdb 데이터베이스가 이미 존재하면 삭제
DROP DATABASE IF EXISTS shopdb;


-- shopdb 데이터베이스 생성 (생성 후 Workbench 스키마 탭 새로고침)
CREATE DATABASE shopdb;


-- shopdb 데이터베이스 사용 (현재 작업 DB로 설정)
USE shopdb;

-- ============================================================
-- [실습 2] 회원 테이블(memberTBL) 생성
-- ============================================================
--
--
-- 컬럼 설계:
--   memberID      : 회원 아이디, CHAR(8), NULL 불가, PK
--   memberName    : 회원 이름,   CHAR(5), NULL 불가
--   memberAddress : 주소,        CHAR(20), NULL 허용
--



-- 실습 :shopdb 안에 memberTBL 테이블 생성하기
CREATE TABLE memberTBL (
    memberID      CHAR(8)  NOT NULL,
    memberName    CHAR(5)  NOT NULL,
    memberAddress CHAR(20) NULL,
    PRIMARY KEY (memberID)
);




-- ============================================================
-- [실습 3] 제품 테이블(productTBL) 생성
-- ============================================================
--
--
-- 컬럼 설계:
--   productName : 제품명,   CHAR(4),  NULL 불가, PK
--   cost        : 가격,     INT,      NULL 불가
--   makeDate    : 제조일자, DATE,     NULL 허용
--   company     : 제조사,   CHAR(5),  NULL 허용
--   amount      : 수량,     INT,      NULL 불가
--

-- 실습 : shopdb 안에  productTBL 테이블 생성하기
CREATE TABLE productTBL (
    productName CHAR(4) NOT NULL,
    cost        INT     NOT NULL,
    makeDate    DATE    NULL,
    company     CHAR(5) NULL,
    amount      INT     NOT NULL,
    PRIMARY KEY (productName)
);





