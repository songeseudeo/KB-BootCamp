-- =========================================================
-- [개념] 네이밍 규칙 (권장 사항)
-- =========================================================
-- MySQL은 테이블/컬럼 이름에 대해 특정 대소문자를 강제하지 않는다.
--
-- 하지만 운영 환경에 따라 대소문자 구분이 다르다:
-- * Windows : 테이블명 대소문자 구분 안 함
-- * Linux   : 테이블명 대소문자 구분 (실무 서버는 대부분 Linux)
--
-- 네이밍 관례:
-- 1. 소문자 사용           : member_tbl (O)  MemberTbl (X)
-- 2. 단어 구분은 언더바    : snake_case  (O)  camelCase (X)
-- 3. 예약어 사용 금지      : user, order, select 등은 테이블명/컬럼명으로 사용 자제
--                            불가피한 경우 백틱(`)으로 감싼다. 예: `order`
-- 4. 의미 있는 이름 사용   : emp_salary (O)  col1 (X)
-- 5. 테이블명에 접미사 활용: member_tbl, product_tbl (팀 규칙에 따라 다름)
-- =========================================================


