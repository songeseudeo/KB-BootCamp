-- =========================================================
-- [개념] DBMS (Database Management System)
-- =========================================================
-- DBMS란?
-- 데이터베이스를 생성하고, 데이터를 저장/조회/수정/삭제할 수
-- 있게 해주는 소프트웨어이다.
-- MySQL은 관계형 DBMS(RDBMS)에 해당한다.
--
-- RDBMS의 핵심 특징:
-- - 데이터를 테이블(행/열) 형태로 저장한다.
-- - 테이블 간의 관계(Relationship)를 정의할 수 있다.
-- - SQL(Structured Query Language)로 데이터를 다룬다.
--
-- 대표적인 RDBMS 제품:
-- MySQL, Oracle, PostgreSQL, MS SQL Server, MariaDB
-- =========================================================

-- =========================================================
-- [개념] SQL 분류
-- =========================================================
-- 1. DDL (Data Definition Language) : 구조 정의
--    - CREATE  : 데이터베이스/테이블 생성
--    - ALTER   : 구조 변경 (컬럼 추가, 타입 변경 등)
--    - DROP    : 데이터베이스/테이블 삭제 (구조+데이터 모두)
--    - TRUNCATE: 테이블 데이터 전체 삭제 (구조는 유지)
--                ※  DDL 성격이며, AUTO_INCREMENT 값도 초기화된다.
--
-- 2. DML (Data Manipulation Language) : 데이터 조작
--    - SELECT : 데이터 조회
--    - INSERT : 데이터 삽입
--    - UPDATE : 데이터 수정
--    - DELETE : 데이터 삭제 (조건부 삭제 가능)
--
-- 3. DCL (Data Control Language) : 권한 제어
--    - GRANT  : 사용자에게 권한 부여
--    - REVOKE : 사용자 권한 회수
--
-- 4. TCL (Transaction Control Language) : 트랜잭션 제어
--    - COMMIT   : 트랜잭션 확정 (변경 사항 영구 저장)
--    - ROLLBACK : 트랜잭션 취소 (변경 사항 되돌리기)
--    - SAVEPOINT: 트랜잭션 내 부분 저장점 설정
--
-- ※ 트랜잭션이란?
--    하나의 논리적인 작업 단위로 묶인 SQL 집합이다.
--    예) 계좌이체 = 출금 UPDATE + 입금 UPDATE -> 둘 다 성공해야 COMMIT
-- =========================================================

