-- =========================================================
-- [개념] world DB
-- =========================================================
-- MySQL 공식 학습용 샘플 데이터베이스이다.
-- 국가(country), 도시(city), 언어(countrylanguage) 정보를 담고 있다.
--
-- 테이블 구조 요약:

-- ==================================================
-- country : 국가 정보 테이블
-- ==================================================
-- Code             : 국가코드 (PK)
-- Name             : 국가명
-- Continent        : 대륙
-- Region           : 지역
-- Population       : 인구수
-- Capital          : 수도(city.ID 참조)

-- 예)
-- KOR / South Korea / Asia / Eastern Asia / 51700000


-- ==================================================
-- city : 도시 정보 테이블
-- ==================================================
-- ID               : 도시번호 (PK)
-- Name             : 도시명
-- CountryCode      : 국가코드 (FK → country.Code)
-- District         : 지역명
-- Population       : 인구수

-- 예)
-- Seoul / KOR / Seoul / 9981619


-- ==================================================
-- countrylanguage : 국가별 언어 정보 테이블
-- ==================================================
-- CountryCode      : 국가코드 (FK → country.Code)
-- Language         : 언어명
-- IsOfficial       : 공식언어 여부 (T/F)
-- Percentage       : 사용비율

-- 예)
-- KOR / Korean / T / 99.9


-- ==================================================
-- 테이블 관계
-- ==================================================

-- country (1) : city (N)
-- 한 국가에는 여러 도시가 존재

-- country.Code = city.CountryCode          (1:N 관계)


-- country (1) : countrylanguage (N)
-- 한 국가에는 여러 언어가 존재

-- country.Code = countrylanguage.CountryCode (1:N 관계)
-- =========================================================

-- [실습 5] world 데이터베이스 선택
use world;


-- [실습 6] world 데이터베이스의 테이블 목록 확인
show tables;


-- [실습 7] world 주요 테이블 구조 확인
-- 실습 : city 테이블 구조 확인하기
-- 또는 축약형: DESC city;
-- ※ DESCRIBE / DESC / SHOW COLUMNS FROM 테이블명 은 동일한 결과를 반환한다.

-- 실습 : country 테이블 구조 확인하기
describe country;
DESC country;


-- 실습 : countrylanguage 테이블 구조 확인하기
describe countrylanguage;
desc countrylanguage;


-- [실습 8] world 데이터 일부 확인
-- 실습 : city 테이블의 모든 컬럼 조회하기 
select *
from city;


-- 실습 : country 테이블의 모든 컬럼 조회하기 
select *
from country;

-- 실습 : countrylanguage 테이블의 모든 컬럼 조회하기 / 조회 개수 제한 -> 5
-- ※ LIMIT n : 상위 n건만 조회한다. (대용량 테이블 탐색 시 필수)
select *
from countrylanguage limit 5;

