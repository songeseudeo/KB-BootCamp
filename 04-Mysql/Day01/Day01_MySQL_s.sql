-- =========================================================
-- [개념] employees DB
-- =========================================================
-- 직원, 부서, 급여, 직책 데이터를 담은 대용량 샘플 데이터베이스이다.
--
-- 테이블 구조 요약:

-- ==================================================
-- employees : 직원 기본 정보 테이블
-- ==================================================
-- emp_no           : 직원번호 (PK)
-- birth_date       : 생년월일
-- first_name       : 이름
-- last_name        : 성
-- gender           : 성별
-- hire_date        : 입사일

-- 예)
-- 10001 / Georgi / Facello / M / 1986-06-26


-- ==================================================
-- departments : 부서 정보 테이블
-- ==================================================
-- dept_no          : 부서번호 (PK)
-- dept_name        : 부서명

-- 예)
-- d001 / Marketing


-- ==================================================
-- dept_emp : 직원-부서 소속 이력 테이블
-- ==================================================
-- emp_no           : 직원번호 (FK → employees.emp_no)
-- dept_no          : 부서번호 (FK → departments.dept_no)
-- from_date        : 부서 시작일
-- to_date          : 부서 종료일

-- 예)
-- 10001 / d001 / 1986-06-26 / 9999-01-01


-- ==================================================
-- dept_manager : 부서 관리자 이력 테이블
-- ==================================================
-- emp_no           : 직원번호 (FK → employees.emp_no)
-- dept_no          : 부서번호 (FK → departments.dept_no)
-- from_date        : 관리자 시작일
-- to_date          : 관리자 종료일

-- 예)
-- 110022 / d001 / 1985-01-01 / 1991-10-01


-- ==================================================
-- salaries : 급여 이력 테이블
-- ==================================================
-- emp_no           : 직원번호 (FK → employees.emp_no)
-- salary           : 급여
-- from_date        : 급여 시작일
-- to_date          : 급여 종료일

-- 예)
-- 10001 / 60117 / 1986-06-26 / 1987-06-26


-- ==================================================
-- titles : 직급(직책) 이력 테이블
-- ==================================================
-- emp_no           : 직원번호 (FK → employees.emp_no)
-- title            : 직급명
-- from_date        : 직급 시작일
-- to_date          : 직급 종료일

-- 예)
-- 10001 / Senior Engineer / 1986-06-26 / 9999-01-01


-- ==================================================
-- 테이블 관계
-- ==================================================

-- employees (1) : salaries (N)
-- 직원 1명은 여러 급여 이력을 가질 수 있음

-- employees (1) : titles (N)
-- 직원 1명은 여러 직급 이력을 가질 수 있음

-- employees (1) : dept_emp (N)
-- 직원 1명은 여러 부서 이력을 가질 수 있음

-- departments (1) : dept_emp (N)
-- 부서 1개에는 여러 직원이 소속될 수 있음

-- employees (1) : dept_manager (N)
-- 직원 1명은 여러 기간 동안 관리자 가능

-- departments (1) : dept_manager (N)
-- 부서 관리자는 기간에 따라 변경 가능


-- ==================================================
-- 핵심 구조
-- ==================================================

-- employees ↔ departments 는 다대다(M:N) 관계
-- → dept_emp 테이블로 연결

-- 모든 이력 테이블은
-- from_date ~ to_date 로 기간 관리
-- ------------------------------------------------------

-- [실습 9] employees 데이터베이스 선택
use employees;


-- [실습 10] employees 데이터베이스의 테이블 목록 확인
show tables;


-- [실습 11] employees 주요 테이블 구조 확인
-- 실습 : employees 테이블 구조 확인하기
desc employees;

-- 실습 : departments 테이블 구조 확인하기
desc departments;

-- 실습 : dept_emp 테이블 구조 확인하기
desc dept_emp;

-- 실습 : salaries 테이블 구조 확인하기
desc salaries;

-- 실습 : titles 테이블 구조 확인하기
desc titles;




-- [실습 12] employees 데이터 일부 확인
-- 실습 : employees 테이블의 모든 컬럼 조회하기 
select * from employees;

-- 실습 : departments 테이블의 모든 컬럼 조회하기 
SELECT * FROM departments;

-- 실습 : salaries 테이블의 모든 컬럼 조회하기 / 조회 개수 제한 -> 5
SELECT * FROM salaries limit 5;

-- 실습 : titles 테이블의 모든 컬럼 조회하기 / 조회 개수 제한 -> 5
SELECT * FROM titles limit 5;

