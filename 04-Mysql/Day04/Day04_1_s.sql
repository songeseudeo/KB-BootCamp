-- [개념] NULL 처리 주의사항
-- =========================================================
-- NULL은 '값이 없음'을 의미한다.
-- NULL은 0이나 빈 문자열('')과 다르다.
--
-- 주의:
-- 1. NULL과의 비교는 =, <> 로 판단하지 않는다.
-- 2. NULL 확인은 IS NULL / IS NOT NULL을 사용한다.
-- 3. COUNT(컬럼명)은 NULL을 제외하고 센다.
-- 4. COUNT(*)는 행 전체 개수를 센다.
--
-- NULL을 표시값으로 바꿀 때 IFNULL(), COALESCE()를 자주 사용한다.
-- COALESCE()는 왼쪽부터 NULL이 아닌 첫 번째 값을 반환한다.

-- =========================================================
-- [실습7-1] NULL 비교와 처리
