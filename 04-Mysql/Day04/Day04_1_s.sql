-- ENUM (Enumeration)
-- ---------------------------------------------------------
-- 정의:
-- - 미리 정의된 값 목록 중 "하나만 선택"해서 저장하는 데이터 타입
--
-- 예:
-- gender ENUM('M','F')
--
-- 내부 동작:
-- - 실제로는 문자열이 아니라 "숫자 인덱스"로 저장됨
--   'M' -> 1
--   'F' -> 2
--
-- 특징:
-- 1. 지정된 값만 입력 가능 (데이터 무결성 보장)
-- 2. 저장 공간 절약 (문자열 대신 숫자 저장)
-- 3. 비교 연산 빠름
--
-- 장점:
-- - 값 제한 가능 (DB 레벨 검증)
-- - 코드 없이도 데이터 규칙 유지
--
-- 단점:
-- - 값 추가/변경 시 ALTER TABLE 필요 (운영 중 위험)
-- - 다른 DBMS와 호환성 낮음
-- - 값이 많아지면 관리 어려움
--
-- 언제 사용?
--  값이 거의 변하지 않는 경우
--  선택지가 적고 명확한 경우
--
--  예:
-- - 성별 ('M','F')
-- - 회원 상태 ('ACTIVE','INACTIVE')
-- - 주문 상태 ('READY','SHIPPED','CANCEL')



