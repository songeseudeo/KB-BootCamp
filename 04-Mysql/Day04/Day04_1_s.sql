-- 문제: productTBL 테이블에서 제조사가 NULL인 상품의 상품명과 제조사 조회하기
--  NULL 비교는 =, <> 대신 IS NULL / IS NOT NULL을 사용한다.

SELECT productName,
       company
FROM productTBL
WHERE company IS NULL;


-- 문제: productTBL 테이블에서 제조사가 NULL일 때 IFNULL과 COALESCE로 '제조사 없음' 표시하기

SELECT productName,
       IFNULL(company, '제조사 없음') AS 'IFNULL 결과',
       COALESCE(company, '제조사 없음') AS 'COALESCE 결과'
FROM productTBL;


