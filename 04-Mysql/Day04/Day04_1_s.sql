-- =========================================================
-- [개념] 명시적 형 변환
-- =========================================================
-- CAST(), CONVERT()를 사용한다.
--
-- 형식:
-- CAST(값 AS 데이터타입)
-- CONVERT(값, 데이터타입)
--
-- 자주 쓰는 변환 타입:
-- CHAR
-- DATE
-- DATETIME
-- DECIMAL
-- SIGNED INTEGER
-- UNSIGNED INTEGER
-- TIME
 
 
 
-- SIGNED INTEGER
-- 부호 있는 정수
-- 음수와 양수 모두 표현 가능
-- 일반적으로 가장 많이 사용
--
--
-- UNSIGNED INTEGER
-- 부호 없는 정수
-- 양수와 0만 표현 가능
-- 음수가 절대 나올 수 없는 값에 사용 (나이, 개수, ID 등)
 
 -- 주의:
-- UNSIGNED INTEGER 에 음수를 변환하면
-- 의도와 전혀 다른 큰 양수값이 나온다.
-- 보통은 SIGNED INTEGER를 기본으로 사용한다.

-- =========================================================
-- [실습5] CAST / CONVERT
-- =========================================================


-- 문제: 문자열 '100'을 정수로 변환한 결과 조회하기
SELECT CAST('100' AS SIGNED INTEGER) AS '문자->정수';


-- 문제: 문자열 '100'을 CONVERT로 정수 변환한 결과 조회하기
--  암시적 형 변환에 의존하지 말고 필요한 경우 CAST/CONVERT로 명시한다.
SELECT CONVERT('100', SIGNED INTEGER) AS '문자->정수';


-- 문제: productTBL 테이블에서 평균 수량을 정수로 변환하여 조회하기
SELECT CAST(AVG(amount) AS SIGNED INTEGER) AS '평균 수량 정수 변환'
FROM productTBL;


-- 문제: productTBL 테이블에서 평균 수량을 CONVERT로 정수 변환하여 조회하기

SELECT CONVERT(AVG(amount), SIGNED INTEGER) AS '평균 수량 정수 변환'
FROM productTBL;


-- 문제: 문자열 '2026-05-05'를 DATE 타입으로 변환한 결과 조회하기

