-- =========================================================
-- [개념] CAST / CONVERT 주의사항
-- =========================================================
-- MySQL 8.4 공식 문서 기준 CAST(), CONVERT()는 값을 지정한 타입으로 변환한다.
--
-- 주의:
-- 1. 변환할 수 없는 값은 0, NULL 또는 경고가 발생할 수 있다.
-- 2. 숫자처럼 보이는 문자열은 숫자 연산에서 자동 변환될 수 있다.
-- 3. 암시적 변환에 의존하기보다 명시적 변환을 사용하는 것이 안전하다.
--
-- 예:
-- '100'은 숫자 100으로 변환 가능
-- '100원'은 숫자 변환 시 의도와 다른 결과가 나올 수 있음

-- =========================================================
-- [실습5-1] CAST 변환 결과 비교
-- =========================================================


-- 문제: 문자열 '100'을 정수로 정상 변환한 결과 조회하기

SELECT CAST('100' AS SIGNED INTEGER) AS '정상 변환';


-- 문제: 문자열 '100원'을 정수로 변환했을 때의 결과 확인하기

SELECT CAST('100원' AS SIGNED INTEGER) AS '부분 변환 주의';


-- 문제: SELECT 문으로 CAST('2026-05-05' AS DATE) AS '날짜 변환' 값 조회하기

SELECT CAST('2026-05-05' AS DATE) AS '날짜 변환';


-- 문제: 문자열 '2026-05-05 10:30:00'을 DATETIME 타입으로 변환한 결과 조회하기

SELECT CAST('2026-05-05 10:30:00' AS DATETIME) AS '날짜시간 변환';

-- =========================================================
-- [개념] 암시적 형 변환
-- =========================================================
-- MySQL은 상황에 따라 자동으로 타입을 변환한다.
--
-- 예:
-- '100' + '200'은 문자열 연결이 아니라 숫자 덧셈으로 처리된다.
-- CONCAT('100', '200')은 문자열 연결로 처리된다.
--
-- 주의:
-- 암시적 형 변환은 편하지만 의도하지 않은 결과가 나올 수 있다.
-- 중요한 쿼리에서는 CAST/CONVERT로 명시하는 것이 안전하다.

-- =========================================================
-- [실습6] 암시적 형 변환
-- =========================================================


-- 문제: 문자열처럼 보이는 숫자 '100'과 '200'을 더한 결과 조회하기

SELECT '100' + '200' AS '문자 덧셈';



-- 문제: CONCAT() 함수로 문자열 '100'과 '200'을 연결한 결과 조회하기
-- CONCAT()은 인수 중 NULL이 있으면 결과가 NULL이 될 수 있다.

SELECT CONCAT('100', '200') AS '문자 연결';
SELECT CONCAT('100', null) AS '문자 연결';



-- 문제: CONCAT() 함수로 숫자 100과 문자열 '200'을 연결한 결과 조회하기

SELECT CONCAT(100, '200') AS '숫자+문자 연결';



-- 문제: 숫자 1이 문자열 '2mega'보다 큰지 비교한 결과 조회하기
-- MySQL이 문자열 '2mega'를 숫자로 변환할 때
-- 앞부분 숫자 2만 읽어서 비교 -> 1 > 2 -> FALSE(0)
SELECT 1 > '2mega' AS '비교1';



-- 문제: 숫자 3이 문자열 '2MEGA'보다 큰지 비교한 결과 조회하기

SELECT 3 > '2MEGA' AS '비교2';



-- 문제: 숫자 0과 문자열 'mega2'가 같은지 비교한 결과 조회하기
-- 'mega2'는 숫자로 시작하지 않아 변환 결과가 0
-- 결국 0 = 0 비교 -> TRUE(1)
SELECT 0 = 'mega2' AS '문자 0 변환 비교';

-- =========================================================
-- [개념] 제어 흐름 함수
-- =========================================================
-- IF(조건, 참일 때 값, 거짓일 때 값)
-- - 조건 결과에 따라 두 값 중 하나를 반환한다.
--
-- IFNULL(값1, 값2) 인수 2개만 받음 MySQL 전용 함수
-- - 값1이 NULL이 아니면 값1 반환
-- - 값1이 NULL이면 값2 반환
--
-- NULLIF(값1, 값2)
-- - 값1과 값2가 같으면 NULL 반환
-- - 다르면 값1 반환
--
--
-- COALESCE(값1, 값2, 값3, ...)
-- - 인수 여러 개 받을 수 있음
-- - 왼쪽부터 NULL이 아닌 첫 번째 값 반환
-- - 표준 SQL 함수 (다른 DBMS에서도 동작)
-- 예:
-- COALESCE(NULL, NULL, '값있음', '두번째') -> '값있음'


-- CASE
-- - 여러 조건에 따라 값을 반환한다.

-- =========================================================
-- [실습7] IF / IFNULL / NULLIF
-- =========================================================


-- 문제: IF() 함수로 100 > 200 조건이 참이면 '참이다', 거짓이면 '거짓이다'를 출력하기

