-- 정수형
-- TINYINT   : 1바이트 정수
-- SMALLINT  : 2바이트 정수
-- INT       : 4바이트 정수
-- BIGINT    : 8바이트 정수

-- 실수형
-- FLOAT  : 4바이트 부동소수점
-- DOUBLE : 8바이트 부동소수점
-- -> 근사값 저장 방식이라 오차가 발생할 수 있음

-- 고정 소수점
-- DECIMAL(M, D)
-- M : 전체 자릿수
-- D : 소수점 이하 자릿수
-- -> 정확한 값 저장 가능

-- 예시
-- DECIMAL(5,2)
-- 전체 5자리, 소수 2자리
-- 저장 가능 예: 123.45

-- =========================================================
-- [중요]
-- FLOAT/DOUBLE은 계산 과정에서 오차가 발생할 수 있다.
-- 따라서 금액, 세금, 정산 데이터는 DECIMAL을 사용하는 것이 좋다.
