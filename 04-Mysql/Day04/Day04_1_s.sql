       COUNT(company) AS 'company가 NULL이 아닌 행 수'
FROM productTBL;
-- =========================================================
-- [개념] CASE 문법
-- =========================================================
-- CASE는 여러 조건에 따라 다른 값을 반환할 때 사용한다.
-- 두 가지 형태가 있다.
--
--
-- ── 형태 1: 단순 CASE ──
-- 하나의 값과 여러 후보를 "=" 로 비교한다.
--
-- CASE 비교할_값
--   WHEN 값1 THEN 결과1
--   WHEN 값2 THEN 결과2
--   ELSE 그_외_결과
-- END
--
-- 예:
-- CASE 등급
--   WHEN 'A' THEN '우수'
--   WHEN 'B' THEN '보통'
--   ELSE '미달'
-- END
--
--
-- ── 형태 2: 검색 CASE ──
-- WHEN 뒤에 조건식을 직접 쓴다.
-- 범위 비교(>, <, BETWEEN 등)에 사용한다.
--
-- CASE
--   WHEN 조건1 THEN 결과1
--   WHEN 조건2 THEN 결과2
--   ELSE 그_외_결과
-- END
--
-- 예:
-- CASE
--   WHEN cost >= 20 THEN '고가'
--   WHEN cost >= 10 THEN '중가'
--   ELSE '저가'
-- END
--
--
-- 주의:
-- 1. CASE 는 반드시 END 로 닫는다.
-- 2. WHEN 은 위에서부터 순서대로 검사한다. 먼저 맞는 조건이 선택된다.
-- 3. ELSE 가 없고 모든 WHEN 이 안 맞으면 NULL 이 반환된다.
-- 4. SELECT 절, WHERE 절, ORDER BY 절 등 어디서든 사용 가능하다.
-- =========================================================
-- [실습8] CASE
-- =========================================================


-- 문제: CASE 문을 사용하여 값 10을 조건에 따라 한글로 분류하기

SELECT CASE 10
         WHEN 1 THEN '일'
         WHEN 5 THEN '오'
         WHEN 10 THEN '십'
         ELSE '기타'
       END AS 'CASE 연습';


