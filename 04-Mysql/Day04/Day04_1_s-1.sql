-- DATETIME
-- - 입력한 값을 그대로 저장
-- - 시간대(Timezone) 영향 없음
-- - 저장 범위: 1000-01-01 ~ 9999-12-31
-- - 저장 크기: 8바이트
--
-- TIMESTAMP
-- - 내부적으로 UTC(전 세계가 공통으로 사용하는 표준 시간 기준)로 변환해서 저장
-- - 조회 시 서버/세션 시간대 기준으로 변환됨
-- - 저장 범위: 1970-01-01 ~ 2038-01-19 (2038년 문제)
-- - 저장 크기: 4바이트
--   데이터베이스가 UTC로 저장해 두면, 어느 지역에서 조회하든 그 지역 시간으로 변환해서 보여줄 수 있다.

-- 차이:
-- DATETIME  -> 고정 시간 저장 (어디서 조회해도 같은 값)
-- TIMESTAMP -> 시간대 따라 값이 바뀔 수 있음 (서버 시간대 바뀌면 표시 값도 바뀜)
--
