-- =========================================================
-- 공식 문서에 따르면 CHAR()는 기본적으로 바이너리 문자열(binary string)을 반환한다. 
-- 그래서 USING 절 없이 사용하면 사람이 읽을 수 있는 문자가 아니라 바이트 값 그대로가 나온다
