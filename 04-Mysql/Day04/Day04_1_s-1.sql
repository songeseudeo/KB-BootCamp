-- SELECT CHAR(65);
-- -- 결과: 0x41   (바이트 값 그대로)

-- -- USING 있을 때: 지정한 문자셋의 실제 문자
-- SELECT CHAR(65 USING utf8mb4);
-- 결과: A



-- 문제: SELECT 문으로 ASCII('A') AS 'A의 코드', CHAR(65) AS '65의 문자' 값 조회하기

SELECT ASCII('A') AS 'A의 코드',
       CHAR(65 USING utf8mb4) AS '65의 문자';



-- =========================================================
-- [개념] LENGTH / CHAR_LENGTH, CONCAT NULL 처리
-- =========================================================
-- LENGTH()
-- - 문자열의 바이트 수를 반환한다.
--
-- CHAR_LENGTH()
-- - 문자열의 문자 수를 반환한다.
--
-- 한글은 문자 수와 바이트 수가 다를 수 있으므로 차이를 확인해야 한다.
-- MySQL 기본 문자셋(utf8mb4) 기준:
--   - 영문/숫자/기호: 1글자 = 1바이트
--   - 한글: 1글자 = 3바이트
--   - 이모지/일부 보조 문자: 1글자 = 4바이트
-- 그래서 '가나다'(3글자)는 CHAR_LENGTH는 3, LENGTH는 9가 나온다.
--
-- CONCAT()
-- - 인수 중 하나라도 NULL이면 결과가 NULL이 될 수 있다.
--
-- CONCAT_WS()
-- - 구분자를 사용하여 문자열을 연결한다.
-- - NULL 값은 건너뛴다.
-- - WS는 "With Separator"의 약자이다.


-- GROUP_CONCAT()
-- - GROUP BY된 여러 행의 값을 하나의 문자열로 연결한다.
-- - 여러 행 -> 하나의 문자열 (집계 함수)


-- =========================================================
-- [실습10-1] 문자 수 / 바이트 수 / NULL 연결 비교
-- =========================================================


-- 문제: SELECT 문으로 LENGTH('ABC') AS 'ABC 바이트', CHAR_LENGTH('ABC') AS 'ABC 문자수' 값 조회하기
-- 영문은 1글자 = 1바이트라서 두 값이 같다. (둘 다 3)

SELECT LENGTH('ABC') AS 'ABC 바이트',
       CHAR_LENGTH('ABC') AS 'ABC 문자수';


-- 문제: SELECT 문으로 LENGTH('가나다') AS '한글 바이트', CHAR_LENGTH('가나다') AS '한글 문자수' 값 조회하기
-- 한글은 utf8mb4에서 1글자 = 3바이트라서 '가나다'(3글자)는 LENGTH=9, CHAR_LENGTH=3 이 된다.
-- VARCHAR(10)에 한글을 저장하면 글자 수 기준이지만, 인덱스 크기는 바이트 기준이다 

SELECT LENGTH('가나다') AS '한글 바이트',
       CHAR_LENGTH('가나다') AS '한글 문자수';


-- 문제: CONCAT에 NULL이 포함될 때 결과가 어떻게 되는지 확인하기
-- CONCAT은 인수 중 하나라도 NULL이면 전체 결과가 NULL이 된다.

SELECT CONCAT('제조사:', NULL, '상품') AS 'CONCAT NULL 결과';


-- 문제: CONCAT_WS가 NULL 값을 건너뛰고 문자열을 연결하는지 확인하기
-- CONCAT_WS는 NULL인 인수를 무시하고 나머지만 구분자로 연결한다.
-- 단, 구분자 자체가 NULL이면 결과도 NULL이 된다.

SELECT CONCAT_WS('-', '제조사', NULL, '상품') AS 'CONCAT_WS NULL 처리';



