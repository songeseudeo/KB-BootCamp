-- 문제: CONCAT_WS() 함수로 연도, 월, 일을 '/'로 연결한 날짜 문자열 조회하기

SELECT CONCAT_WS('/', '2026', '05', '05') AS '날짜 문자열';


-- 문제: productTBL 테이블에서 제조사와 상품명을 '-'로 연결하여 조회하기

