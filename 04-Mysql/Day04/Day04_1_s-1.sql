-- =========================================================
-- [개념] 문자열 함수 5: LEFT / RIGHT / UPPER / LOWER / LPAD / RPAD
-- =========================================================
-- LEFT(문자열, 길이)
-- - 왼쪽에서 길이만큼 반환
--
-- RIGHT(문자열, 길이)
-- - 오른쪽에서 길이만큼 반환
--
-- UPPER(), LOWER()
-- - 대문자/소문자 변환
-- - 한글에는 대소문자 개념이 없어서 그대로 반환된다.
--
-- LPAD(), RPAD()
-- - 지정 길이까지 문자열을 채움
-- - LPAD = Left Padding (왼쪽 채움), RPAD = Right Padding (오른쪽 채움)
-- - 회원번호, 주문번호 등 자릿수 맞춰 표시할 때 유용하다. 예: '00000123'

-- =========================================================
-- [실습14] LEFT / RIGHT / UPPER / LOWER / LPAD / RPAD
-- =========================================================


-- 문제: 문자열 'abcdefghi'의 왼쪽 3글자와 오른쪽 3글자를 조회하기

SELECT LEFT('abcdefghi', 3) AS 'LEFT',
       RIGHT('abcdefghi', 3) AS 'RIGHT';



-- 문제: 문자열 'mysql'은 대문자로, 'MYSQL'은 소문자로 변환한 결과 조회하기

SELECT UPPER('mysql') AS '대문자',
       LOWER('MYSQL') AS '소문자';



-- 문제: 문자열 'SQL'의 왼쪽과 오른쪽을 '*' 문자로 채워 전체 길이를 10자로 만든 결과 조회하기
-- 결과: '*******SQL' (왼쪽 채움), 'SQL*******' (오른쪽 채움)

SELECT LPAD('SQL', 10, '*') AS '왼쪽 채움',
       RPAD('SQL', 10, '*') AS '오른쪽 채움';

