-- =========================================================
-- [개념] 문자열 함수 7: REPEAT / REPLACE / REVERSE / SPACE / SUBSTRING
-- =========================================================
-- REPEAT(문자열, 횟수)
-- - 문자열 반복
--
-- REPLACE(문자열, 원래문자열, 바꿀문자열)
-- - 문자열 치환 (모든 일치 항목 다 바뀜)
-- - 대소문자 구분함
--
-- REVERSE(문자열)
-- - 문자열 순서 반전
--
-- SPACE(길이)
-- - 길이만큼 공백 반환
--
-- SUBSTRING(문자열, 시작, 길이)
-- - 시작 위치부터 길이만큼 반환
-- - 위치는 1부터 시작
-- - 길이 생략 시 끝까지 추출
--
-- SUBSTRING_INDEX(문자열, 구분자, 횟수)
-- - 구분자를 기준으로 문자열 일부 반환
-- - 양수: 왼쪽에서 N번째 구분자까지
-- - 음수: 오른쪽에서 N번째 구분자까지
-- - 도메인이나 경로 파싱에 자주 쓰인다.

-- =========================================================
-- [실습16] REPEAT / REPLACE / REVERSE / SPACE / SUBSTRING
-- =========================================================


-- 문제: 문자열 'SQL'을 3번 반복한 결과 조회하기

SELECT REPEAT('SQL', 3) AS '반복';



-- 문제: 문자열 '이것이 MySQL이다'에서 '이것이'를 'This is'로 변경한 결과 조회하기

SELECT REPLACE('이것이 MySQL이다', '이것이', 'This is') AS '치환';



-- 문제: 문자열 'MySQL'을 거꾸로 뒤집은 결과 조회하기

SELECT REVERSE('MySQL') AS '반전';



-- 문제: 문자열 'A'와 'B' 사이에 공백 5개를 넣은 결과 조회하기

SELECT CONCAT('A', SPACE(5), 'B') AS '공백';



-- 문제: 문자열 '대한민국만세'에서 3번째 위치부터 2글자를 추출한 결과 조회하기
-- 결과: '민국' (3번째 글자부터 2글자)

SELECT SUBSTRING('대한민국만세', 3, 2) AS 'SUBSTRING';



-- 문제: 문자열 'www.mysql.com'에서 '.' 기준 왼쪽 2개와 오른쪽 2개를 추출한 결과 조회하기
-- 양수 2: 왼쪽부터 2번째 '.' 까지   -> 'www.mysql'
-- 음수 -2: 오른쪽부터 2번째 '.' 까지 -> 'mysql.com'

SELECT SUBSTRING_INDEX('www.mysql.com', '.', 2) AS '왼쪽 기준',
       SUBSTRING_INDEX('www.mysql.com', '.', -2) AS '오른쪽 기준';

