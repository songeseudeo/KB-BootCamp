-- =========================================================
-- [실습11] GROUP_CONCAT
-- =========================================================


-- 문제: productTBL 테이블에서 제조사별 상품 목록을 쉼표(,)로 연결하여 조회하기
-- 일반 집계 함수(SUM, COUNT)는 숫자를 합치지만, GROUP_CONCAT은 문자열을 합친다.
SELECT company,
       GROUP_CONCAT(productName SEPARATOR ', ') AS '상품 목록'
FROM productTBL
GROUP BY company;


-- =========================================================
-- [개념] GROUP_CONCAT 주의사항
-- =========================================================
-- GROUP_CONCAT()은 그룹별 값을 하나의 문자열로 합칠 때 사용한다.
--
-- 주의:
-- 1. NULL 값은 결과에서 제외된다.
-- 2. ORDER BY를 함께 사용하면 연결 순서를 지정할 수 있다.
-- 3. DISTINCT를 사용하면 중복을 제거할 수 있다.

-- =========================================================
-- [실습11-1] GROUP_CONCAT 정렬 / 중복 제거
-- =========================================================


-- 문제: productTBL 테이블에서 제조사별 상품 목록을 상품명 오름차순으로 정렬하여 하나의 문자열로 조회하기

SELECT company,
       GROUP_CONCAT(productName ORDER BY productName SEPARATOR ', ') AS '상품 목록'
FROM productTBL
GROUP BY company;


-- 문제: productTBL 테이블에서 중복을 제거한 제조사 목록을 오름차순 문자열로 조회하기

SELECT GROUP_CONCAT(DISTINCT company ORDER BY company SEPARATOR ', ') AS '제조사 목록'
FROM productTBL;

-- =========================================================
-- [개념] 문자열 함수 3: 위치 찾기
-- =========================================================
-- ELT(위치, 값1, 값2, ...)
-- - 위치 번째 문자열 반환 (1부터 시작)
--
-- FIELD(찾을값, 값1, 값2, ...)
-- - 찾을 값의 위치 반환, 없으면 0
-- - ELT와 정반대 역할 (ELT는 위치->값, FIELD는 값->위치)
--
-- FIND_IN_SET(찾을값, '값1,값2,...')
-- - 콤마로 구분된 문자열 리스트에서 위치 반환
-- - FIELD와 비슷하지만 두 번째 인수가 콤마로 구분된 하나의 문자열이라는 점이 다르다.
--
-- INSTR(기준문자열, 찾을문자열)
-- - 찾을 문자열의 시작 위치 반환 (없으면 0)
--
-- LOCATE(찾을문자열, 기준문자열)
-- - INSTR과 비슷하지만 파라미터 순서가 반대
-- - 위치 = 1부터 시작 (프로그래밍 언어의 0부터와 다름)

-- =========================================================
-- [실습12] ELT / FIELD / FIND_IN_SET / INSTR / LOCATE
-- =========================================================

-- 문제: SELECT 문으로 목록의 2번째 값 조회하기

SELECT ELT(2, '하나', '둘', '셋') AS 'ELT 결과';



-- 문제: SELECT 문으로 '둘'의 위치 번호 조회하기

SELECT FIELD('둘', '하나', '둘', '셋') AS 'FIELD 결과';



-- 문제: SELECT 문으로 문자열 목록에서 '둘'의 위치 조회하기

SELECT FIND_IN_SET('둘', '하나,둘,셋') AS 'FIND_IN_SET 결과';



-- 문제: SELECT 문으로 '이것이 MySQL이다' 문자열에서 'MySQL'의 시작 위치 조회하기

SELECT INSTR('이것이 MySQL이다', 'MySQL') AS 'INSTR 결과';



-- 문제: SELECT 문으로 '이것이 MySQL이다' 문자열에서 'MySQL'의 시작 위치 조회하기

SELECT LOCATE('MySQL', '이것이 MySQL이다') AS 'LOCATE 결과';

