-- - YEAR, MONTH, DAY, HOUR, MINUTE, SECOND 등 원하는 단위로 차이를 계산한다.
-- - 사용법: TIMESTAMPDIFF(단위, 시작, 끝)  <- 순서 주의 (DATEDIFF와 반대)
