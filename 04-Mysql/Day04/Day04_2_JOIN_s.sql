-- =========================================================
-- [개념] OUTER JOIN에서 ON과 WHERE 위치 차이
-- =========================================================
-- INNER JOIN에서는 ON 조건과 WHERE 조건 차이가 작게 느껴질 수 있다.
-- 하지만 OUTER JOIN에서는 조건 위치에 따라 결과가 달라질 수 있다.
--
-- LEFT JOIN에서 오른쪽 테이블 조건을 WHERE에 쓰면
-- 매칭되지 않은 NULL 행이 제거되어 INNER JOIN처럼 동작할 수 있다.
--
-- 왼쪽 테이블의 모든 행을 유지하려면
-- 오른쪽 테이블 조건은 ON에 두는 것이 적절한 경우가 많다.

-- =========================================================
-- [실습5-1] OUTER JOIN 조건 위치 비교
-- =========================================================

-- 문제: 구매 가격이 100 이상인 상품만 연결하되,
-- 구매 이력이 없는 회원도 함께 조회하기

-- 참고:
-- LEFT JOIN의 오른쪽 테이블 조건을 ON 절에 작성하면
-- 왼쪽 테이블의 모든 회원 정보를 유지할 수 있다.
-- 결과: 회원 전체 출력, 조건 안 맞으면 b 컬럼이 NULL







-- 문제: 구매 가격이 100 이상인 구매 정보만 조회하기

-- 참고:
-- LEFT JOIN 후 WHERE 절에 오른쪽 테이블 조건을 작성하면
-- NULL 행이 제거되어 INNER JOIN처럼 동작할 수 있다.
-- 결과: 조건 안 맞는 회원은 사라짐 (구매 이력 없는 회원도 빠짐)







-- 위와 결과가 같다. INNER JOIN은 NULL 행을 만들지 않음
SELECT u.userID,
       u.name,
       b.prodName,
       b.price
FROM usertbl u
INNER JOIN buytbl b
ON u.userID = b.userID
WHERE b.price >= 100
ORDER BY u.userID;


-- 아래 쿼리는 INNER JOIN으로 만들 수 없다.
-- INNER JOIN은 양쪽 테이블에 매칭되는 행만 결과로 만든다.
-- 매칭이 안 되는 행은 아예 결과에 등장하지 않으므로,
-- NULL인 행 자체가 생기지 않는다.
SELECT u.userID, u.name, b.prodName, b.price
FROM usertbl u
LEFT JOIN buytbl b
ON u.userID = b.userID
WHERE b.price IS NULL;

