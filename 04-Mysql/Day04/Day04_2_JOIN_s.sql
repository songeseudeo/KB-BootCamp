-- =========================================================
-- [개념] JOIN이란?
-- =========================================================
-- JOIN은 두 개 이상의 테이블을 연결하여 하나의 결과 집합으로 만드는 것이다.
--
-- 관계형 데이터베이스는 중복과 공간 낭비를 줄이기 위해
-- 데이터를 여러 테이블로 나누어 저장한다.
--
-- 분리된 테이블은 공통된 키를 기준으로 연결한다.
-- 주로 PRIMARY KEY와 FOREIGN KEY 관계를 사용한다.

-- =========================================================
-- [개념] 테이블 별칭 (Alias)
-- =========================================================
-- 긴 테이블명을 짧게 줄여서 쿼리를 깔끔하게 만든다.
-- FROM 절에 "테이블명 별칭" 형식으로 작성한다.
-- AS는 생략 가능하다.
--
-- 예시:
-- FROM usertbl u            -- usertbl을 u로 부른다 (AS 생략)
-- FROM usertbl AS u         -- 명시적 표기
--
-- SELF JOIN에서는 같은 테이블을 두 번 쓰므로 별칭이 필수다.

-- =========================================================
-- [개념] JOIN 종류
-- =========================================================
-- INNER JOIN
-- - 양쪽 테이블에 모두 존재하는 데이터만 조회
--
-- LEFT OUTER JOIN
-- - 왼쪽 테이블의 모든 데이터를 조회
-- - 오른쪽에 일치하는 데이터가 없으면 NULL
--
-- RIGHT OUTER JOIN
-- - 오른쪽 테이블의 모든 데이터를 조회
-- - 왼쪽에 일치하는 데이터가 없으면 NULL
--
-- CROSS JOIN
-- - 두 테이블의 모든 조합
-- - 곱집합
--
-- SELF JOIN
-- - 자기 자신 테이블을 조인
-- - 상사/부하 관계 같은 계층 구조에 사용
--
-- FULL OUTER JOIN
-- - 양쪽 전체 데이터를 모두 조회
-- - MySQL은 FULL OUTER JOIN을 직접 지원하지 않는다.
-- - 필요한 경우 LEFT JOIN과 RIGHT JOIN을 UNION으로 조합한다.

-- =========================================================
-- [개념] ON vs WHERE
-- =========================================================
-- ON
-- - 테이블을 어떻게 연결할지 정하는 조인 조건
--
-- WHERE
-- - 조인 결과 중 필요한 행만 필터링하는 조건
--
-- 특히 OUTER JOIN에서는 ON 조건과 WHERE 조건 위치에 따라 결과가 달라질 수 있다.

-- =========================================================
-- [개념] JOIN 논리 흐름
-- =========================================================
-- 실제 SQL 실행 순서:
-- FROM → ON → JOIN → WHERE → GROUP BY → HAVING → SELECT → DISTINCT → ORDER BY → LIMIT
--
-- 핵심:
-- 1. 먼저 테이블을 가져온다 (FROM)
-- 2. ON 조건으로 행을 매칭한다
-- 3. JOIN 종류에 따라 매칭 안된 행도 포함할지 결정한다
-- 4. WHERE로 필요한 결과만 걸러낸다
-- 5. GROUP BY로 그룹핑한다
-- 6. SELECT로 최종 컬럼을 선택한다
-- 7. ORDER BY로 정렬한다

