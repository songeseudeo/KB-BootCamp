-- =========================================================
-- [개념] CROSS JOIN
-- =========================================================
-- 두 테이블의 모든 조합을 반환한다.
-- 조인 조건이 필요 없다.
--
-- 결과 행 수:
-- 첫 번째 테이블 행 수 × 두 번째 테이블 행 수
--
-- 주의:
-- 1만 행 × 1만 행 = 1억 행이 되어 서버가 멈출 수 있다.
-- 큰 테이블에는 절대 실수로 사용하지 않도록 주의한다.
-- 보통 옵션/카테고리 조합 같은 작은 테이블끼리만 사용한다.
--
-- 사용 예:
-- 색상 × 사이즈 상품 옵션 조합

-- =========================================================
-- [실습13] CROSS JOIN
-- =========================================================


