-- =========================================================
-- [개념] FULL OUTER JOIN
-- =========================================================
-- MySQL은 FULL OUTER JOIN을 직접 지원하지 않는다.
-- 비슷한 결과가 필요하면 LEFT JOIN과 RIGHT JOIN을 UNION으로 합친다.

-- =========================================================
-- [실습14] FULL OUTER JOIN 대체 예시
-- =========================================================

-- 방법 1: LEFT JOIN + RIGHT JOIN을 UNION으로 합치기
-- 양쪽 결과의 중복은 UNION이 자동으로 제거한다.
SELECT u.userID,
       u.name,
       b.prodName
FROM usertbl u
LEFT JOIN buytbl b
ON u.userID = b.userID

UNION

SELECT u.userID,
       u.name,
       b.prodName
FROM usertbl u
RIGHT JOIN buytbl b
ON u.userID = b.userID;


-- 방법 2: LEFT JOIN 전체 + 왼쪽에 없는 오른쪽 행만 추가
SELECT u.userID, u.name, b.prodName
FROM usertbl u
LEFT JOIN buytbl b ON u.userID = b.userID

UNION

SELECT u.userID, u.name, b.prodName
FROM usertbl u
RIGHT JOIN buytbl b ON u.userID = b.userID
WHERE u.userID IS NULL;

-- =========================================================
-- [개념] UNION / UNION ALL
-- =========================================================
-- UNION
-- - 두 SELECT 결과를 합친다.
-- - 중복 행은 제거된다.
--
-- UNION ALL
-- - 두 SELECT 결과를 합친다.
-- - 중복 행도 그대로 포함한다.
--
-- 주의사항:
-- 1. 각 SELECT의 컬럼 수가 같아야 한다.
-- 2. 각 컬럼의 데이터 타입이 호환되어야 한다.
-- 3. 결과 컬럼명은 첫 번째 SELECT의 컬럼명을 따른다.
-- 4. ORDER BY는 마지막 SELECT 뒤에 한 번만 쓸 수 있다.

-- =========================================================
