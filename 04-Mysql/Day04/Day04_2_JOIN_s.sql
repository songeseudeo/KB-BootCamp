-- =========================================================
-- 학생, 동아리, 학생-동아리 관계 테이블을 만든다.
--
-- 한 학생은 여러 동아리에 가입할 수 있다.
-- 한 동아리에는 여러 학생이 가입할 수 있다.
-- 따라서 학생과 동아리 사이에는 N:M(다대다) 관계가 생긴다.
--
-- N:M 관계는 데이터베이스가 직접 표현하지 못하므로
-- 중간 테이블(연결 테이블, junction table)을 만들어
-- 1:N + N:1 관계로 나눠 표현한다.
--
-- 학생(1) ── (N)학생_동아리(N) ── (1)동아리
--
-- 중간 테이블의 PRIMARY KEY는 보통 (student_id, club_id)처럼
-- 복합키로 설정해 중복 가입을 방지한다.

-- =========================================================
