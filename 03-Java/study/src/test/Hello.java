package test;
// 자바 파일 하나는 클래스 파일이라고 함
// 파일명 첫 글자는 무조건 대문자
// 파일 확장자는 무조건 .java
public class Hello {
    //시작되는 함수(method)를 지정해줘야 함
    //java에서 public static void main() 메서드를
    //시작 메서드로 인식
    public static void main(String[] args) {
        //컴퓨터에 출력으로 메세지를 보여라
        //문자열은 "" 안에 사용
        System.out.println("hello world");
        System.out.println("자동 완성");
    }
}
