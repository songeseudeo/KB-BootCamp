// 아래 순서대로 실행해서 결과를 확인한다.
//
// 1. 홍길동 계좌 생성 (초기 잔고 없음)
// 2. 김길동 계좌 생성 (초기 잔고 50000원)
// 3. 전체 계좌 수 출력
// 4. 홍길동 계좌에 30000원 입금
// 5. 홍길동 계좌에 10000원 출금
// 6. 홍길동 계좌에 50000원 출금 시도 → 잔고 부족 확인
// 7. 홍길동 계좌에 -1000원 입금 시도 → 유효성 검사 확인
// 8. 홍길동, 김길동 계좌 정보 출력
// 9. 전체 계좌 잔고 합계 출력
//
// ===============================
// [예상 실행 결과]
//
// 전체 계좌 수: 2
//
// [입금] 30000원 입금. 잔고: 30000원
// [출금] 10000원 출금. 잔고: 20000원
// 잔고 부족. 현재 잔고: 20000원
// 입금 금액은 0보다 커야 합니다.
//
// (계좌 정보 출력 - 본인이 구성한 형식대로)
//
// 전체 잔고 합계: 70000원
//
// ===============================
// [체크리스트]
//
// - 필드가 모두 private 으로 선언되었는가?
// - 생성자 2개가 this() 로 연결되어 있는가?
// - deposit(), withdraw() 에 유효성 검사가 있는가?
// - deposit(), withdraw() 에서 totalBalance 도 함께 변경되는가?
// - count, totalBalance 가 private static 으로 선언되었는가?
// - getCount(), getTotalBalance() 가 static 메소드로 선언되었는가?
// - 계좌번호가 "ACC-1", "ACC-2" 형식으로 자동 생성되는가?
// ===============================
public class AccountExample {
    public static void main(String[] args) {

        // 1. 홍길동 계좌 생성 (초기 잔고 없음)
        Account hong = new Account("홍길동");

        // 2. 김길동 계좌 생성 (초기 잔고 50000원)
        Account kim = new Account("김길동", 50000);

        // 3. 전체 계좌 수 출력
        System.out.println("전체 계좌 수: " + Account.getCount());

        // 4. 홍길동 계좌에 30000원 입금
        hong.deposit(30000);

        // 5. 홍길동 계좌에 10000원 출금
        hong.withdraw(10000);

        // 6. 홍길동 계좌에 50000원 출금 시도 (잔고 부족 확인)
        hong.withdraw(50000);

        // 7. 홍길동 계좌에 -1000원 입금 시도 (유효성 검사 확인)
        hong.deposit(-1000);

        // 8. 홍길동, 김길동 계좌 정보 출력
        System.out.println(hong);
        System.out.println(kim);

        // 9. 전체 계좌 잔고 합계 출력
        System.out.println("전체 잔고 합계: " + Account.getTotalBalance() + "원");
    }
}