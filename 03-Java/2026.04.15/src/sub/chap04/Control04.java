package com.kb.c_chap04.s;

/*
 * ===============================
 * 제어문 - for 반복문
 * ===============================
 *
 * 반복문(loop)은 같은 코드를 여러 번 반복 실행하기 위한 제어문이다.
 *
 * for 문은 반복 횟수가 명확할 때 사용한다.
 *
 * for (초기식; 조건식; 증감식) {
 *     반복 실행할 코드;
 * }
 *
 * 실행 순서
 * 1) 초기식 (딱 1회만 실행)
 * 2) 조건식 검사
 * 3) true → 블록 실행
 * 4) 증감식 실행
 * 5) 2번으로 돌아가서 반복
 * 6) 조건식이 false → 반복문 종료
 */
public class Control04 {

    public static void main(String[] args) {

        // ===============================
        // 1. 기본 for 문 - 1부터 10까지 출력
        // ===============================
        /*
         * i = 1 로 시작해서
         * i <= 10 인 동안 반복하고
         * 한 번 반복할 때마다 i++ 로 1씩 증가한다.
         */
        for(int i=1; i <=10; i++) {
            System.out.print(i + " ");
        }

        System.out.println();
        System.out.println("--------------------------------");

        // ===============================
        // 2. 1부터 100까지 합계
        // ===============================
        /*
         * 반복이 끝난 뒤 n 값은 101이 되어있다.
         * 그래서 마지막으로 더한 수는 n - 1 이다.
         */
        int sum = 0;
        for(int i=1; i<=100; i++) {
            sum += i;
        }
        System.out.println("합출력 :" + sum);
        System.out.println("--------------------------------");

        // ===============================
        // 3. 역순 출력 - 감소 증감식
        // ===============================
        /*
         * 증감식에 i-- 를 사용하면 역순으로 반복할 수 있다.
         */
        for(int j=10; j>=1; j--){
            System.out.println(j + " ");
        }

        System.out.println();
        System.out.println("--------------------------------");

        // ===============================
        // 4. 짝수만 출력
        // ===============================
        /*
         * i += 2 로 2씩 증가시키면
         * if 없이도 짝수만 뽑을 수 있다.
         */

        for (int k = 2; k <= 20; k += 2) {
            System.out.print(k + " ");
        }

        System.out.println();
        System.out.println("--------------------------------");



        // ===============================
        // 5. 중첩 for 문 - 구구단
        // ===============================
        /*
         * 반복문 안에 또 다른 반복문을 넣을 수 있다.
         * 바깥 for 문이 1번 실행될 때
         * 안쪽 for 문이 처음부터 끝까지 모두 실행된다.
         *
         * 바깥 for 문: 단(dan) - 2단부터 9단
         * 안쪽 for 문: 곱해지는 수(su) - 1부터 9
         */

        for (int dan = 2; dan <= 9; dan++) {
            for (int su = 1; su <= 9; su++) {
                System.out.println(dan + " x " + su + " = " + (dan * su));
            }
        }
        System.out.println("--------------------------------");

        // ===============================
        // 6. 난수를 활용한 for 문
        // ===============================
        /*
         * Math.random() 으로 주사위를 5번 굴린다.
         * 난수 생성 방법에 대한 자세한 내용은 Control08 을 참고한다.
         */

        System.out.println("[주사위 5번 굴리기]");

        for (int t = 1; t <= 5; t++) {
            int dice = (int) (Math.random() * 6) + 1; // 1~6 난수 (Control08 참고)
            System.out.println(t + "번째: " + dice);
        }

        System.out.println("--------------------------------");


    }

}