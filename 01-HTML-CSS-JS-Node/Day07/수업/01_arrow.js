/* 

https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Functions/Arrow_functions
화살표 함수 (Arrow Function)

ES6에서 도입된 화살표 함수는 function 키워드 대신 => 를 사용해
함수를 더 간단하게 표현하는 문법이다.

화살표 함수는 주로
1. 짧은 함수 표현
2. 콜백 함수 작성
3. 배열 메서드(map, filter 등)에서 많이 사용한다.

----------------------------------------
1. 기본 문법
----------------------------------------
() => { ... }           // 매개변수가 없을 때
x => { ... }            // 매개변수가 1개일 때 소괄호 생략 가능
(x, y) => { ... }       // 매개변수가 여러 개일 때 소괄호 필요

----------------------------------------
2. 함수 몸체 작성 방법
----------------------------------------
x => { return x * x; }  // 일반적인 형태
x => x * x              // 실행문이 한 줄이면 중괄호 생략 가능
                        // 이 경우 return도 자동으로 적용된다.

----------------------------------------
3. 객체 반환
----------------------------------------
() => ({ a: 1 })        // 객체 리터럴 반환 시 소괄호로 감싸야 한다.

주의!
() => { a: 1 }          // 객체가 아니라 함수 몸체로 해석된다.

----------------------------------------
4. 멀티라인
----------------------------------------
(x, y) => {
    const result = x + y;
    return result;
}

실행문이 여러 줄이면 중괄호를 생략할 수 없다.
*/


/* 
----------------------------------------
 1. 기존 함수 표현식과 화살표 함수 비교
----------------------------------------
*/

// 기존 함수 표현식
let message = function () {
    return "Hello World!";
};

console.log(message());

// 화살표 함수
message = () => {
    return "Arrow Function!";
};

console.log(message());


/* 
----------------------------------------
2. 실행문이 한 줄이면 중괄호와 return 생략 가능
----------------------------------------
*/

message = () => "Arrow Functions are Simple!";
console.log(message());

/* 
----------------------------------------
2-1. 실행문이 여러 줄인 경우
----------------------------------------
중괄호와 return을 생략할 수 없다.
*/

let getSum = (num1, num2) => {
    const result = num1 + num2;
    return result;
};

console.log("두 수의 합:", getSum(10, 20));
/* 
----------------------------------------
3. 매개변수가 여러 개인 경우
----------------------------------------
소괄호를 반드시 작성해야 한다.
*/

message = (val1, val2) => "Arrow " + val1 + val2;
console.log(message("Function", "!"));


/* 
----------------------------------------
4. 매개변수가 한 개인 경우
----------------------------------------
소괄호를 생략할 수 있다.
*/

message = val => "Arrow " + val;
console.log(message("Functions are GOOD!!!"));


/* 
----------------------------------------
5. 삼각형 넓이 구하기
공식: (밑변 * 높이) / 2
----------------------------------------
*/

// 일반 형태
let getTriangle = (base, height) => {
    return base * height / 2;
};

console.log("삼각형의 넓이:", getTriangle(5, 2));

// 축약 형태
getTriangle = (base, height) => base * height / 2;
console.log("삼각형의 넓이:", getTriangle(10, 4));


/* 
----------------------------------------
실습 6. 객체 리터럴 반환
----------------------------------------
객체를 반환할 때는 소괄호로 감싸야 한다.
*/

const createUser = (id, name) => ({ id, name });
console.log(createUser(1, "유관순"));

/* 
객체를 반환하지 못하는 잘못된 형태
중괄호를 함수 몸체로 인식하기 때문에 undefined가 반환된다.
*/
const createUser2 = (id, name) => { id, name };
console.log(createUser2(2, "홍길동"));

/* 
----------------------------------------
 배열과 함께 사용하는 화살표 함수
----------------------------------------
화살표 함수는 콜백 함수로 자주 사용된다.

실습 7. filter와 함께 사용하기
----------------------------------------
짝수만 추출
*/

const numbers = [1, 2, 3, 4, 5, 6];
// const evenNumbers = numbers.filter(function(num) {
//     return num % 2 === 0;
// });
const evenNumbers = numbers.filter(num => num % 2 === 0);
console.log("짝수만 추출:", evenNumbers);




/* 
----------------------------------------
정리
----------------------------------------

1. 화살표 함수는 function 대신 => 를 사용한다.
2. 매개변수가 1개일 때는 소괄호 생략 가능하다.
3. 매개변수가 없거나 여러 개일 때는 소괄호가 필요하다.
4. 실행문이 한 줄이면 중괄호와 return 생략 가능하다.
5. 실행문이 여러 줄이면 중괄호와 return을 작성해야 한다.
6. 객체를 반환할 때는 소괄호로 감싸야 한다.
7. 화살표 함수는 콜백 함수에서 자주 사용된다.
*/



